    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
    String strAntiga, strNova, strRepete; 
    
    strAntiga = jPasswordAntiga.getText();
    strNova = jPasswordNova.getText();
    strRepete = jPasswordRepeti.getText();
    
    if(strAntiga.equals(usuario.getSenha()) && strNova.equals(strRepete)){
        
        try {
           int opcao;
           opcao = JOptionPane.showConfirmDialog(null,"Deseja Realmente Alterar a senha?","Atenc√£o",JOptionPane.YES_NO_OPTION);  
           if(opcao == JOptionPane.YES_OPTION){ 
               setAlteraSenha(String.valueOf(usuario.getId()), strRepete);
                
               jPasswordAntiga.setText("");
               jPasswordNova.setText("");
               jPasswordRepeti.setText("");                
                
               JFrameMenu JFrameMenu = new JFrameMenu();
               JFrameMenu.setVisible(true);
               dispose();
             }else{
               jPasswordAntiga.setText("");
               jPasswordNova.setText("");
               jPasswordRepeti.setText("");
           } 

           
        } catch (SQLException ex) {
            Logger.getLogger(JFrameAlteraSenha.class.getName()).log(Level.SEVERE, null, ex);
        }
        System.out.println("teste");
    }else{
            JFrame frame = new JFrame("Alerta");
            JOptionPane.showMessageDialog(frame, "Verifique os campos a cima!");
    }
    }//GEN-LAST:event_btnGravarActionPerformed

