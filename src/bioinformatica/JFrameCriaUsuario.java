    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
    String strUsuario, strSenha, strRepete; 
    
    strUsuario = jTextUsuario.getText();
    strSenha = jPasswordSenha.getText();
    strRepete = jPasswordRepeti.getText();
    
    if(!strUsuario.equals("") && !strUsuario.equals(null) && strSenha.equals(strRepete)){
        
        try {
           int opcao;
           opcao = JOptionPane.showConfirmDialog(null,"Deseja Realmente Cadastrar Um Novo Usuario?","Atenc√£o",JOptionPane.YES_NO_OPTION);  
           if(opcao == JOptionPane.YES_OPTION){ 
               setNovoUsuario(strUsuario, strRepete);
                
               jTextUsuario.setText("");
               jPasswordSenha.setText("");
               jPasswordRepeti.setText("");                
                
               JFrameMenu JFrameMenu = new JFrameMenu();
               JFrameMenu.setVisible(true);
               dispose();
             }else{
               jTextUsuario.setText("");
               jPasswordSenha.setText("");
               jPasswordRepeti.setText("");
           } 

           
        } catch (SQLException ex) {
            Logger.getLogger(JFrameCriaUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }

    }else{
            JFrame frame = new JFrame("Alerta");
            JOptionPane.showMessageDialog(frame, "Verifique os campos a cima!");
    }
    }//GEN-LAST:event_btnGravarActionPerformed

